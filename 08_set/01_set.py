variable01 = {} # it empty dictonary

variable02 = {"name" : "Sujit Tomar", "roll" : "0252", "course" : "B.Tech"} # it is dictonary

variable03 = {1, 2, 3, 4,} # its called set

variable04 = set() # it is called empty set

print(type(variable01)) #  <class 'dict'>
print(type(variable02)) #  <class 'dict'>
print(type(variable03)) #  <class 'set'>
print(type(variable04)) #  <class 'set'>

# A set cannot contain duplicate elements. If you try to add a duplicate, it will simply be ignored.
variable01 = {10, 20, 30, 10, 20, 30, 40, 50}
print(variable01) # {50, 20, 40, 10, 30}





